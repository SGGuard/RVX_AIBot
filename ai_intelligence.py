def analyze_user_knowledge_level(
    xp: int,
    level: int,
    courses_completed: int,
    tests_passed: int,
    recent_topic: Optional[str] = None
) -> UserLevel:
    """
    –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —É—Ä–æ–≤–µ–Ω—å –∑–Ω–∞–Ω–∏–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏.
    
    Args:
        xp: –û–ø—ã—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        level: –£—Ä–æ–≤–µ–Ω—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        courses_completed: –ó–∞–≤–µ—Ä—à–µ–Ω–æ –∫—É—Ä—Å–æ–≤
        tests_passed: –ü—Ä–æ–π–¥–µ–Ω–æ —Ç–µ—Å—Ç–æ–≤
        recent_topic: –ü–æ—Å–ª–µ–¥–Ω–∏–π –∏–∑—É—á–µ–Ω–Ω—ã–π —Ç–æ–ø–∏–∫
    
    Returns:
        UserLevel - –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–π —É—Ä–æ–≤–µ–Ω—å
    """
    if xp < 100 and level == 1 and courses_completed == 0:
        return UserLevel.ABSOLUTE_BEGINNER
    elif xp < 500 and level <= 2 and courses_completed < 2:
        return UserLevel.BEGINNER
    elif xp < 2000 and level <= 4 and courses_completed < 4:
        return UserLevel.INTERMEDIATE
    elif xp < 5000 and level <= 7:
        return UserLevel.ADVANCED
    else:
        return UserLevel.EXPERT


