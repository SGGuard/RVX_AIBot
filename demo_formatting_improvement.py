def print_comparison(example):
    """Печатает сравнение до и после форматирования"""
    
    title = example["title"]
    markdown = example["markdown"]
    html = markdown_to_html(markdown)
    
    print("\n" + "=" * 100)
    print(f"  {title}")
    print("=" * 100)
    
    print("\n❌ БЫЛО (старый подход - убирали форматирование):")
    print("-" * 100)
    # Имитируем старое поведение - убираем маркеры
    old_text = markdown.replace("**", "").replace("__", "").replace("~~", "").replace("`", "")
    print(f"  {old_text}")
    print("-" * 100)
    print("  ⚠️ ПРОБЛЕМА: Текст как 'стена' - глаз не может зацепиться за суть")
    
    print("\n✅ СТАЛО (новый подход - HTML форматирование):")
    print("-" * 100)
    print(f"  {html}")
    print("-" * 100)
    print("  ✨ УЛУЧШЕНИЯ:")
    
    # Считаем количество выделенного текста
    bold_count = html.count("<b>")
    underline_count = html.count("<u>")
    strike_count = html.count("<s>")
    code_count = html.count("<code>")
    
    improvements = []
    if bold_count > 0:
        improvements.append(f"• {bold_count} ключевых терминов выделены <b>жирным</b>")
    if underline_count > 0:
        improvements.append(f"• {underline_count} важных фраз подчеркнуты <u>линией</u>")
    if strike_count > 0:
        improvements.append(f"• {strike_count} элементов зачеркнуто для контраста")
    if code_count > 0:
        improvements.append(f"• {code_count} значений выделены как <code>код</code>")
    
    for improvement in improvements:
        print(f"    {improvement}")
    
    return len(improvements) > 0


